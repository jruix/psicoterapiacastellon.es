		<p>Hoy en día contamos con multitud de métodos terapéuticos. El por qué, de 
		entre todos ellos, elegí la orientación gestáltica en mi trabajo es bien simple: porque  
		me la creo. Y este es el consejo que doy a estudiantes o psicólogos noveles cuando 
		me preguntan qué método elegir. Aquel que te creas, les explico. Porque si crees en 
		él, podrás transmitirlo, porque si te identificas con ese modo de entender a las 
		personas y al mundo, desde ese lugar, podrás ayudar a que otras personas 
		encuentren la fuerza y el camino para su sanación. Por supuesto, la elección 
		dependerá en parte de la personalidad de cada terapeuta, de que sintonice con 
		aquel modelo de trabajo que debe “hacer suyo”. Dentro de la corriente con la que se 
		sienta más afín, cada profesional creará su propio estilo de ser terapeuta.</p>
		<p>Pero, permítanme hablarles de mi experiencia y de cómo llegué a creerme esta 
		orientación. Mi primer contacto con la terapia gestáltica fue en Diciembre de 1993, a 
		través de un curso titulado “del miedo al poder interno”, que impartía Vicente 
		Cuevas. Preguntado por mis expectativas para aquel taller expresé la demanda de 
		lograr un autoapoyo tal, que me permitiera no depender de las demás personas, ser 
		totalmente <em>autosuficiente</em>. Después de escucharme, Vicente Cuevas, en lugar de 
		presentarme una explicación racional y bien argumentada sobre el tema, pidió a todo 
		el grupo que se diera la vuelta y me mostrara la espalda. El grupo siguió la consigna. 
		Al verme en aquella situación, me di realmente cuenta que necesitaba a las demás 
		personas para vivir y que tenía un concepto equivocado de lo que era el autoapoyo.</p>
		<p>Lo vivido a lo largo de aquel curso fue tan significativo para mí, que tomé la 
		determinación de volver a aquella escuela y aprender aquel método. Antes de ello 
		pasaron varios años durante los que cursé mis estudios de tercer ciclo en la 
		Universidad de Salamanca, dentro del programa “sexualidad, familia y pareja”.</p>
		<p>Por cierto, somos seres sociales. Recordemos que, en su pirámide de 
		necesidades, Maslow coloca la necesidad de pertenencia en tercer lugar, justo 
		después de las <em>necesidades fisiológicas</em> como comer o beber, y de las <em>necesidades de seguridad y protección</em>. Nadie puede autoapoyarse tanto que no necesite a las 
		demás personas. De la misma manera que un excesivo apoyo en las otras personas, 
		nos lleva a la dependencia, un excesivo apoyo en un@ mism@, nos lleva al 
		aislamiento, y no se crece en aislamiento. El apoyo es una necesidad fundamental, 
		unas veces nos apoyamos más en nosotr@s mism@s y otras, por ejemplo en 
		periodos de crisis, nos apoyamos más en el exterior. Ambas fuentes de apoyo son 
		complementarias y no excluyentes. Como nos enseña la Dra. Ilse Gschwend, el 
		dogma de la autorrealización contiene un error fatal, que no es otro que el intento de 
		lograr la autorrealización sin contar con los demás. Según la Dra. Ilse, esto lleva a un 
		callejón sin salida, porque sin las aportaciones de los demás, yo me atrofio<sup>1</sup>.</p>
		<p>El autoapoyo tiene que ver con creer en las propias capacidades para 
		solucionar las dificultades que nos plantea la vida, con dejarme sentir lo que siento, pensar lo que pienso, permitirme mi propio ritmo personal. Todo lo cual no excluye, 
		para nada, tomar en consideración las opiniones de l@s demás, ni la ayuda que me 
		pueda llegar desde fuera.</p>
		<p>Pero, volvamos al tema central del artículo. Vicente Cuevas me hubiera podido 
		ofrecer una explicación sensata y bien argumentada sobre el tema (de la cual 
		disponía), pero ello no hubiera desembocado en una verdadera toma de conciencia 
		o, en todo caso, hubiera resultado una enseñanza que, con el tiempo, 
		probablemente hubiera olvidado. Sin embargo, proponiéndome aquella experiencia, 
		de un modo vivencial y sentido con todo mi cuerpo (y no sólo con la razón) me di 
		cuenta de aspectos importantes para mí, que, como pueden comprobar, 18 años 
		después, aún conservo.</p>
		<p>Esta es una de las características de la terapia gestáltica, prioriza <strong>“la 
		búsqueda experimental de las soluciones”</strong> S. Ginger (2005), por encima de la 
		búsqueda racional, más propia de terapias cognitivo­-conductuales, que much@s 
		psicólog@s estudiamos en la universidad.</p>
		<p>La <strong>concepción del ser humano</strong> que me ofrecía la universidad me quedaba 
		corta. Siempre creí que las personas somos algo más que pensamientos y 
		conductas. Fue una grata sorpresa encontrar esa mirada gestáltica, más global e 
		integradora, para mí más real, que atiende y que tiene en cuenta las diferentes 
		dimensiones de la persona: racional, emocional, corporal, social y espiritual.
		Uno de los principios clave y, para mí, muy liberador, fue el concepto de la 
		<strong>integración de las polaridades</strong>. Voy a tratar de explicarlo. Verán, cuando nos 
		describimos, la mayoría de nosotr@s solemos clasificarnos en categorías 
		excluyentes, es decir, nos definimos como activ@s o como pasiv@s, como 
		extravertid@s o como introvertid@s, como valientes o cobardes... De modo que 
		cada uno de estos polos es incompatible con el otro (o eres una cosa o eres la otra, 
		solemos pensar; no se puede ser valiente y cobarde a la vez, por ejemplo) y 
		además, un polo es valorado como positivo y deseable, mientras que el otro es 
		considerado como negativo (se cree que es mejor ser activ@ que pasiv@, mejor ser 
		valiente que cobarde). Esto lleva a la persona a verse de forma unilateral, 
		estereotipada y limitada.</p>
		<p>Desde una orientación gestáltica, en cambio, entendemos que todo tiene dos 
		polos. Y que ambos polos son dos extremos de la misma cosa, es una cuestión de 
		grados. Para explicar esto me gusta citar el ejemplo del día y la noche como dos 
		polos, dos contrarios, que, en realidad, se complementan. Uno no se entiende sin el 
		otro. Uno no puede existir sin el otro. Podemos decir que ambos extremos son lo mismo en distintos grados de manifestación. Un polo es la presencia, mientras que 
		el otro la ausencia de luz. Así ocurre con las características de personalidad. Un 
		autoconcepto saludable es aquel que integra las polaridades, es decir, aquel que 
		incluye ambos polos y me permite comprender que, en ocasiones puedo ser activ@ 
		y en otras pasiv@, en determinadas circunstancias atrevido, y en otras situaciones 
		mostrarme más precavido... en mayor o menor grado, soy todo. Este aprendizaje lo 
		sentí y lo siento como liberador.</p>
		<p>Otra interesante aportación que encontré en la Gestalt fue la explicación de 
		cómo las personas llegamos a resolver nuestras necesidades. Me gusta el modo en que lo plantea Michel Katzeff, que propone un <strong>ciclo de satisfacción de las necesidades</strong> de siete fases. Serían estas:</p>
		<ul>
			<li><strong>Sensación.</strong> En esta primera etapa, percibimos determinadas sensaciones, 
			pero no podemos nombrarlas, todavía no sabemos qué es lo que nos 
			ocurre. Es en esta etapa del ciclo cuando muchas personas acuden a 
			terapia.</li>
			<li><strong>Conciencia.</strong> La persona reconoce con nitidez aquello que necesita. Puede 
			ser algo sencillo, como por ejemplo, me doy cuenta que tengo sed. Pero, 
			en ocasiones no tenemos una visión clara de aquello que nos ocurre y gran 
			parte del trabajo, durante las sesiones de terapia, consistirá precisamente 
			en ayudar a tomar conciencia de la propia necesidad.</li>
			<li><strong>Energetización.</strong> Una buena toma de conciencia, moviliza nuestra energía.</li>
			<li><strong>Acción.</strong> A continuación, con esa energía disponible, emprendo una acción 
			clara dirigida a resolver la necesidad. Siguiendo nuestro ejemplo, me dirijo 
			al frigorífico, donde sé que hay bebida.</li>
			<li><strong>Contacto.</strong> En este momento estoy en contacto con lo que necesito: estoy 
			bebiendo.</li>
			<li><strong>Satisfacción.</strong> Mi necesidad ha sido satisfecha. Lo disfruto. Sé que no 
			necesito beber más. Tengo suficiente.</li>
			<li><strong>Retirada.</strong> Estoy disponible para un nuevo ciclo, una nueva necesidad que 
			pueda surgir.</li>
		</ul>
		<p>Lo interesante es que la Gestalt nos explica también cómo las personas 
		podemos interrumpir este ciclo, lo que nos lleva a no satisfacer las propias 
		necesidades. Dotado de este instrumento, el trabajo del terapeuta, consistirá en 
		realizar un buen diagnóstico, un buen encuadre, detectar las interrupciones y ayudar 
		a la persona a tomar conciencia de ellas y de cómo puede concluir o cerrar sus 
		ciclos pendientes.</p>
		<p>Finalmente, especialmente atractivo me resultó el <strong>respeto al ritmo y al estilo 
		personal</strong>. Como escribe Serge Ginger (2005) “la Gestalt subraya el derecho a la 
		diferencia y valora la especificidad de cada cual”. Lo cual significa que ni todas las personas tenemos las mismas necesidades, ni la misma forma de solucionarlas.</p>
		<p>Como psicólogo no tengo la solución para todo (es más, no creo que nadie la 
		tenga). Pero, tomando el método gestáltico, dispongo de una manera de estar, de 
		una manera de mirar (a la persona que tengo enfrente y al mundo que nos rodea), 
		que me ayuda a poder entender aquello que, dentro del particular contexto de cada 
		cuál, le puede ser terapéutico o saludable. Es, con la seguridad de contar con esta 
		visión, de contar con esta herramienta, por lo que, en una primera sesión de 
		psicoterapia, puedo sentarme frente a la persona y preguntarle, "qué le trate por aquí", 
		"en qué cree que le puedo ayudar". Ahora bien, todo ello no puede servir de excusa 
		para quedarse ahí. La formación y la revisión del terapeuta debe ser continua, 
		porque, como bien dicen los Polster, “el terapeuta es su propio instrumento de 
		trabajo”, Erving y Miriam Polster (2001).</p>
		<p>Un abrazo!</p>
